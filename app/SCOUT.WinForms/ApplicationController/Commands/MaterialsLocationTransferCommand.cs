using System;
using SCOUT.WinForms.Materials;

namespace SCOUT.WinForms.Core
{
    /// <summary>
    /// Loads the materials location transfer form
    /// </summary>
    public class MaterialsLocationTransferCommand : Command
    {
        public override void Execute()
        {
            ViewLoader.Instance()
                .CreateFormWithArgs<MaterialsLocationTransferForm>(false, null);

        }
    }
}