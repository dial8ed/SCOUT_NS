namespace SCOUT.Core.Data
{
    public class UnitFailure
    {
        private int m_id;
        private string m_failCode;
        private string m_failDescription;
        private string m_notes;

        public int Id
        {
            get { return m_id; }
            set { m_id = value; }
        }

        public string FailCode
        {
            get { return m_failCode; }
            set { m_failCode = value; }
        }

        public string FailDescription
        {
            get { return m_failDescription; }
            set { m_failDescription = value; }
        }

        public string Notes
        {
            get { return m_notes; }
            set { m_notes = value; }
        }
    }
}